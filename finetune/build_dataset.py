"""
build_dataset.py — Synthesize the SEBI behavioral-analysis instruction-tuning
dataset from the 30 hand-written gold examples in seed_examples.jsonl.

Strategy
--------
The 30 seeds cover the canonical patterns (Revenge / FOMO / Over-Leveraging /
Addiction / Healthy) across all four languages and the full risk-score range.
We expand to ~250 training examples by deterministic perturbation:

    • Numeric jitter  — losses ±1, total_loss_inr ±15%, margin_pct ±5%
    • Vow-list shuffle — same vows, different order
    • Vow-list dropout — randomly drop one or two vows from the list
    • Language rotation — each scenario seen in EN + at least one Indic language
    • Seed_for_history — slight history_sessions / history_high_risk_rate jitter

The response is regenerated by re-running the deterministic scoring rubric the
live prompt uses, NOT by an LLM. This keeps the dataset clean and the labels
faithful. Only the wording of nudge_message / nudge_message_local stays from
the seed (where present), so the model learns Mumbai-investor-advisor phrasing.

Output: data/sebi_train.jsonl + data/sebi_test.jsonl (90/10 stratified by pattern).

Usage
-----
    python build_dataset.py --out data/sebi_train.jsonl
"""

from __future__ import annotations

import argparse
import hashlib
import json
import random
from collections import defaultdict
from pathlib import Path

SEED = 42
EXPANSION_PER_SEED = 8           # 30 seeds × 8 = 240 examples; +30 = 270
TEST_SPLIT = 0.10
SCRIPT_DIR = Path(__file__).parent

# Pattern → score adjustment baselines (must match ai_engine.py rubric)
SCORE_RUBRIC = {
    "two_or_more_losses": 200,
    "four_or_more_losses": 200,
    "margin_above_70": 150,
    "vow_violated": 200,
    "historical_risk_above_50": 150,
    "last_trade_was_win": -100,
    "cap": 1000,
}


def compute_score(scenario: dict) -> int:
    """Re-score a scenario per the live rubric so labels stay faithful."""
    score = 0
    if scenario["losses"] >= 2: score += SCORE_RUBRIC["two_or_more_losses"]
    if scenario["losses"] >= 4: score += SCORE_RUBRIC["four_or_more_losses"]
    if scenario["margin_pct"] > 70: score += SCORE_RUBRIC["margin_above_70"]

    # Vow check — count how many were violated
    n_violated = count_vows_violated(scenario)
    score += n_violated * SCORE_RUBRIC["vow_violated"]

    if scenario["history_high_risk_rate"] > 0.5:
        score += SCORE_RUBRIC["historical_risk_above_50"]
    if scenario["last_trade_outcome"] == "win":
        score += SCORE_RUBRIC["last_trade_was_win"]
    return min(SCORE_RUBRIC["cap"], max(0, score))


def count_vows_violated(scenario: dict) -> int:
    """Heuristic vow-violation counter that matches the seed labeling."""
    count = 0
    for vow in scenario["vows"]:
        v = vow.lower()
        if "stop after" in v and scenario["losses"] >= 2: count += 1
        elif "exceed 50%" in v or "exceed 50 %" in v:
            if scenario["margin_pct"] > 50: count += 1
        elif "revenge" in v and scenario["losses"] >= 3: count += 1
        elif "fomo" in v and scenario["losses"] >= 3: count += 1
        elif "averaging down" in v and scenario["losses"] >= 3: count += 1
        elif "1 lakh" in v and scenario["margin_pct"] > 70: count += 1
        elif "trading after 2pm" in v: count += 1   # crude proxy
        elif "options" in v and scenario["losses"] >= 2: count += 1
        elif "expiry day" in v and scenario["losses"] >= 2: count += 1
        elif "5k loss" in v and scenario["losses"] >= 3: count += 1
        elif "spouse" in v and scenario["losses"] >= 4: count += 1
        elif "review chart" in v and scenario["losses"] >= 3: count += 1
        elif "sleep" in v and scenario["losses"] >= 4: count += 1
    return count


def derive_pattern(scenario: dict, score: int) -> str:
    """Pattern selection mirrors the live prompt's rubric."""
    if score < 600:
        return "Healthy Trading"
    if scenario["history_sessions"] >= 15 and scenario["history_high_risk_rate"] >= 0.5:
        return "Addiction Loop"
    if scenario["margin_pct"] > 70 and scenario["losses"] < 4:
        return "Over-Leveraging"
    for vow in scenario["vows"]:
        if "fomo" in vow.lower():
            return "FOMO"
    return "Revenge Trading"


def derive_risk_level(score: int) -> str:
    if score < 400: return "low"
    if score < 600: return "medium"
    return "high"


def derive_crisis(scenario: dict, score: int) -> int:
    """Heuristic crisis score (0-100) — matches the rubric in ai_engine.py."""
    crisis = 0
    crisis += min(40, abs(scenario["total_loss_inr"]) // 500)
    if scenario["margin_pct"] > 80: crisis += 25
    if scenario["history_high_risk_rate"] > 0.7: crisis += 25
    if scenario["losses"] >= 6: crisis += 15
    return min(100, crisis)


def jitter(scenario: dict, rng: random.Random) -> dict:
    """Numeric perturbations that keep the example realistic."""
    s = dict(scenario)
    s["losses"] = max(0, s["losses"] + rng.choice([-1, 0, 0, 1]))
    s["total_loss_inr"] = int(s["total_loss_inr"] * (1 + rng.uniform(-0.15, 0.15)))
    s["margin_pct"] = max(0, min(100, s["margin_pct"] + rng.randint(-5, 5)))
    s["history_sessions"] = max(0, s["history_sessions"] + rng.randint(-2, 3))
    s["history_high_risk_rate"] = max(0.0, min(1.0,
        s["history_high_risk_rate"] + rng.uniform(-0.08, 0.08)))
    return s


def vary_vows(vows: list[str], rng: random.Random) -> list[str]:
    """Shuffle the vow order; occasionally drop one to broaden coverage."""
    v = list(vows)
    rng.shuffle(v)
    if len(v) > 2 and rng.random() < 0.3:
        v.pop(rng.randrange(len(v)))
    return v


def synthesize_response(seed_resp: dict, scenario: dict) -> dict:
    """Recompute response fields per the live rubric, preserving seed nudge text."""
    score = compute_score(scenario)
    pattern = derive_pattern(scenario, score)
    risk = derive_risk_level(score)
    crisis = derive_crisis(scenario, score)
    n_violated = count_vows_violated(scenario)

    # Pull violated vows back out of the original list — order-preserving
    violated = []
    for vow in scenario["vows"]:
        if count_vows_violated({**scenario, "vows": [vow]}) > 0:
            violated.append(vow)
        if len(violated) >= n_violated:
            break

    # Keep the seed's nudge wording for high-risk; blank for low/medium
    nudge = seed_resp["nudge_message"] if score > 600 else ""
    nudge_local = seed_resp["nudge_message_local"] if score > 600 else ""

    return {
        "behavioral_score": score,
        "risk_level": risk,
        "detected_pattern": pattern,
        "nudge_message": nudge,
        "nudge_message_local": nudge_local,
        "vows_violated": violated,
        "crisis_score": crisis,
        "sebi_disclosure": seed_resp["sebi_disclosure"],
    }


def format_for_gemma(example: dict) -> dict:
    """Wrap the example in Gemma's chat template."""
    scenario = example["scenario"]
    response = example["response"]

    user_prompt = (
        "You are Finsight OS, a behavioral guardian for retail F&O traders in India.\n"
        "Analyze this trading session and reply with the JSON schema only.\n\n"
        f"DATA\n"
        f"Losses this session: {scenario['losses']} | Total loss: ₹{scenario['total_loss_inr']:.0f}\n"
        f"Margin used: {scenario['margin_pct']}%\n"
        f"Vows: {scenario['vows']}\n"
        f"History: {scenario['history_sessions']} sessions, "
        f"{scenario['history_high_risk_rate']*100:.0f}% high-risk rate\n"
        f"Last trade: {scenario['last_trade_outcome']}\n"
        f"Preferred language: {scenario['language']}\n"
    )
    response_str = json.dumps(response, ensure_ascii=False)
    text = (
        f"<start_of_turn>user\n{user_prompt}<end_of_turn>\n"
        f"<start_of_turn>model\n{response_str}<end_of_turn>"
    )
    return {"text": text}


def build(seeds_path: Path, out_train: Path, out_test: Path) -> None:
    rng = random.Random(SEED)
    out_train.parent.mkdir(parents=True, exist_ok=True)

    seeds = [json.loads(line) for line in seeds_path.read_text(encoding="utf-8").splitlines() if line.strip()]
    print(f"[build] Loaded {len(seeds)} seed examples")

    # 1. Keep all seeds verbatim
    examples: list[dict] = list(seeds)

    # 2. Expand each seed via jittering
    for seed in seeds:
        for _ in range(EXPANSION_PER_SEED):
            scenario = jitter(seed["scenario"], rng)
            scenario["vows"] = vary_vows(scenario["vows"], rng)
            response = synthesize_response(seed["response"], scenario)
            examples.append({"scenario": scenario, "response": response})

    print(f"[build] After expansion: {len(examples)} examples")

    # 3. Stratified train/test split — keep pattern distribution
    by_pattern: dict[str, list] = defaultdict(list)
    for ex in examples:
        by_pattern[ex["response"]["detected_pattern"]].append(ex)

    train, test = [], []
    for pattern, group in by_pattern.items():
        rng.shuffle(group)
        n_test = max(1, int(len(group) * TEST_SPLIT))
        test.extend(group[:n_test])
        train.extend(group[n_test:])

    rng.shuffle(train)
    rng.shuffle(test)

    # 4. Write JSONL with Gemma chat template
    with out_train.open("w", encoding="utf-8") as f:
        for ex in train:
            f.write(json.dumps(format_for_gemma(ex), ensure_ascii=False) + "\n")
    with out_test.open("w", encoding="utf-8") as f:
        for ex in test:
            f.write(json.dumps(ex, ensure_ascii=False) + "\n")

    # 5. Hash for reproducibility
    hash_path = out_train.parent / "dataset_hash.txt"
    h = hashlib.sha256()
    h.update(out_train.read_bytes())
    h.update(out_test.read_bytes())
    hash_path.write_text(f"{h.hexdigest()}  {len(train)}+{len(test)} examples\n")

    print(f"[build] Wrote {len(train)} train + {len(test)} test examples")
    print(f"[build] Dataset SHA-256: {h.hexdigest()[:16]}…  → {hash_path}")
    print("[build] Pattern distribution (train):")
    train_dist: dict[str, int] = defaultdict(int)
    for line in out_train.read_text(encoding="utf-8").splitlines():
        # Parse the embedded JSON response back out for the count
        if '"detected_pattern":' in line:
            for p in ("Revenge Trading", "FOMO", "Over-Leveraging",
                      "Addiction Loop", "Panic Selling", "Healthy Trading"):
                if f'"{p}"' in line:
                    train_dist[p] += 1
                    break
    for p, c in sorted(train_dist.items(), key=lambda kv: -kv[1]):
        print(f"  {p:20s} {c:4d}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--seeds", default=str(SCRIPT_DIR / "seed_examples.jsonl"))
    parser.add_argument("--out", default=str(SCRIPT_DIR / "data" / "sebi_train.jsonl"))
    parser.add_argument("--test-out", default=str(SCRIPT_DIR / "data" / "sebi_test.jsonl"))
    args = parser.parse_args()
    build(Path(args.seeds), Path(args.out), Path(args.test_out))
